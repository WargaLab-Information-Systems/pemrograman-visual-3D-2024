    private void apilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_apilihActionPerformed
        // TODO add your handling code here:
    String namaObat = null;
    String gejala = null;
    String detail = null;
    String gambar = null;

    if (obat1.isSelected()) {
        namaObat = "Aminophylline";
        gejala = "Sesak Napas";
        detail = "Aminophylline digunakan untuk melebarkan saluran udara di paru-paru dan mengatasi masalah sesak napas.";
        gambar = "/amino.png";
    } else if (obat2.isSelected()) {
        namaObat = "Antalgin";
        gejala = "Nyeri";
        detail = "Antalgin digunakan untuk meredakan nyeri ringan hingga sedang, termasuk sakit kepala dan sakit gigi.";
        gambar = "/antal.png";
    } else if (obat3.isSelected()) {
        namaObat = "Antasida";
        gejala = "Lambung";
        detail = "Antasida digunakan untuk menetralisir asam lambung dan mengatasi rasa nyeri yang disebabkan oleh asam lambung.";
        gambar = "/antas.jpg";
    } else if (obat4.isSelected()) {
        namaObat = "Ambroxol";
        gejala = "Batuk";
        detail = "Ambroxol adalah obat yang digunakan untuk mengencerkan dahak pada saluran pernapasan.";
        gambar = "/ambroxol.jpg";
    } else if (obat5.isSelected()) {
        namaObat = "Atorvastatin";
        gejala = "Menurunkan Kolesterol";
        detail = "Atorvastatin membantu menurunkan kadar kolesterol jahat dan meningkatkan kolesterol baik dalam darah.";
        gambar = "/Atorvastatin.jpg";
    } else if (obat6.isSelected()) {
        namaObat = "Bisoprolol";
        gejala = "Jantung";
        detail = "Bisoprolol digunakan untuk mengobati tekanan darah tinggi dan penyakit jantung tertentu.";
        gambar = "/bisoprolol.jpg";
    } else if (obat7.isSelected()) {
        namaObat = "Albendazole";
        gejala = "Cacingan";
        detail = "Albendazole digunakan untuk mengobati infeksi cacing di dalam tubuh.";
        gambar = "/albendazole.jpg";
    } else if (obat8.isSelected()) {
        namaObat = "CTM";
        gejala = "Alergi";
        detail = "CTM (Chlorpheniramine Maleate) digunakan untuk meredakan gejala alergi seperti bersin dan mata berair.";
        gambar = "/ctm.jpg";
    } else if (obat9.isSelected()) {
        namaObat = "Alpara";
        gejala = "Flu";
        detail = "Alpara digunakan untuk mengatasi gejala flu seperti hidung tersumbat, sakit kepala, dan demam.";
        gambar = "/alpara.jpg";
    } else if (obat10.isSelected()) {
        namaObat = "Betahistine";
        gejala = "Pusing";
        detail = "Betahistine digunakan untuk mengatasi pusing dan gangguan telinga bagian dalam.";
        gambar = "/betahistine.png";
    } else {
        JOptionPane.showMessageDialog(this, "Silakan pilih obat terlebih dahulu!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    ImageIcon icon = new ImageIcon(getClass().getResource(gambar));
    String info = "<html><b>Nama Obat:</b> " + namaObat + "<br>"
                + "<b>Gejala:</b> " + gejala + "<br>"
                + "<b>Detail:</b> " + detail + "</html>";
    
    JOptionPane.showMessageDialog(this, info, "Informasi Obat", JOptionPane.INFORMATION_MESSAGE, icon);

    }//GEN-LAST:event_apilihActionPerformed

