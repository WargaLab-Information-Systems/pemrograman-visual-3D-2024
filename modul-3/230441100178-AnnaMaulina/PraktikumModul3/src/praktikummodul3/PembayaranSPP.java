    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String namaSiswa = namaTF.getText();
        int sppPerBulan = Integer.parseInt(sppTF.getText());
        int totalBulan = 0;
        StringBuilder bulanYangDibayar = new StringBuilder();
        JCheckBox[] bulanCheckBoxes = {januari, februari, maret, april, 
                                mei, juni, juli, agustus, 
                                september, oktober, november, desember};

        for (JCheckBox checkBox : bulanCheckBoxes) {
            if (checkBox.isSelected()) {
                totalBulan++;
                bulanYangDibayar.append(checkBox.getText()).append(", ");
                statusPembayaran.put(checkBox.getText(), true);
            }
        }

        int totalPembayaran = totalBulan * sppPerBulan;
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        model.addRow(new Object[]{namaSiswa, bulanYangDibayar.toString(), sppPerBulan, totalPembayaran});
        JOptionPane.showMessageDialog(this, "Pembayaran berhasil disimpan!");

        updateCheckboxStates(namaSiswa);
    }//GEN-LAST:event_jButton4ActionPerformed

    private void updateCheckboxStates(String namaSiswa) {
    JCheckBox[] bulanCheckBoxes = {januari, februari, maret, april, 
                            mei, juni, juli, agustus, 
                            september, oktober, november, desember};
    
    for (JCheckBox checkBox : bulanCheckBoxes) {
        if (statusPembayaran.get(checkBox.getText())) {
            checkBox.setEnabled(false);  
        }
    }
}
    
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int sppPerBulan = Integer.parseInt(sppTF.getText()); 
        int totalTunggakan = 0;
        boolean adaTunggakan = false;
        StringBuilder bulanBelumLunas = new StringBuilder();

        for (String bulan : statusPembayaran.keySet()) {
            if (!statusPembayaran.get(bulan)) {
                adaTunggakan = true;
                bulanBelumLunas.append(bulan).append(", ");
                totalTunggakan += sppPerBulan; 
            }
        }

        if (adaTunggakan) {
            JOptionPane.showMessageDialog(this, "Status: Menunggak\nBulan yang belum lunas: " 
                + bulanBelumLunas.toString() + "\nTotal Tunggakan: Rp " + totalTunggakan);
        } else {
            JOptionPane.showMessageDialog(this, "Status: Sudah Lunas. Tidak ada tunggakan.");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

