    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int sppPerBulan = Integer.parseInt(sppTF.getText());
        int totalBulan = 0;
        StringBuilder bulanYangDibayar = new StringBuilder();
        JCheckBox[] bulanCheckBoxes = {januari, februari, maret, april, 
                               mei, juni, juli, agustus, 
                               september, oktober, november, desember};
        for (JCheckBox checkBox : bulanCheckBoxes) {
            if (checkBox.isSelected()) {
                totalBulan++;
                bulanYangDibayar.append(checkBox.getText()).append(" ");
            }
        }

        int totalPembayaran = totalBulan * sppPerBulan;
        totalTF.setText(String.valueOf(totalPembayaran));
    }//GEN-LAST:event_jButton1ActionPerformed

