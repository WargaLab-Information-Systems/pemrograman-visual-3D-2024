        if (cbseptember.isSelected()) { totalBulan++; bulanDipilih.add("September"); }
        if (cboktober.isSelected()) { totalBulan++; bulanDipilih.add("Oktober"); }
        if (cbnovember.isSelected()) { totalBulan++; bulanDipilih.add("November"); }
        if (cbdesember.isSelected()) { totalBulan++; bulanDipilih.add("Desember"); }

        if (totalBulan == 0) {
            JOptionPane.showMessageDialog(this, "Silakan pilih setidaknya satu bulan.", "Input Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int totalSPP = totalBulan * 200000;
        txt5.setText(String.valueOf(totalSPP));

        List<String> bulanSudahDibayar = new ArrayList<>();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        for (String bulan : bulanDipilih) {
            boolean sudahLunas = false;
            for (int i = 0; i < model.getRowCount(); i++) {
                Object bulanDiTabelObj = model.getValueAt(i, 0);
                Object statusDiTabelObj = model.getValueAt(i, 1);
                if (bulanDiTabelObj != null && statusDiTabelObj != null) {
                    String bulanDiTabel = bulanDiTabelObj.toString();
                    String statusDiTabel = statusDiTabelObj.toString();
                    if (bulan.equals(bulanDiTabel) && statusDiTabel.equals("Sudah Lunas")) {
                        sudahLunas = true;
                        bulanSudahDibayar.add(bulan);
                        break;
                    }
                }
            }
            if (!sudahLunas) {
                bulanMenunggak.add(bulan);
            }
        }

        int sisaSaldo = jumlah;
        String status;

        for (String bulan : bulanMenunggak) {
            if (sisaSaldo >= 200000) {
                sisaSaldo -= 200000;
                status = "Sudah Lunas";
            } else {
                status = "Menunggak";
            }

            boolean bulanSudahAda = false;
            for (int i = 0; i < model.getRowCount(); i++) {
                Object bulanDiTabelObj = model.getValueAt(i, 0);
                if (bulan.equals(bulanDiTabelObj)) {
                    bulanSudahAda = true;
                    model.setValueAt(status, i, 1);
                    break;
                }
            }

            if (!bulanSudahAda) {
                model.addRow(new Object[]{bulan, status});
            }

            if (status.equals("Sudah Lunas")) {
                switch (bulan) {
                    case "Januari": cbjanuari.setEnabled(false); break;
                    case "Februari": cbfebruari.setEnabled(false); break;
                    case "Maret": cbmaret.setEnabled(false); break;
                    case "April": cbapril.setEnabled(false); break;
                    case "Mei": cbmei.setEnabled(false); break;
                    case "Juni": cbjuni.setEnabled(false); break;
                    case "Juli": cbjuli.setEnabled(false); break;
                    case "Agustus": cbagustus.setEnabled(false); break;
                    case "September": cbseptember.setEnabled(false); break;
                    case "Oktober": cboktober.setEnabled(false); break;
                    case "November": cbnovember.setEnabled(false); break;
                    case "Desember": cbdesember.setEnabled(false); break;
                }
            }
        }

        if (sisaSaldo >= 0) {
            txt4.setText(String.valueOf(sisaSaldo));
            
            JOptionPane.showMessageDialog(this, "Pembayaran berhasil! Sisa saldo: " + sisaSaldo, "Sukses", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Uang tidak cukup untuk melunasi semua bulan menunggak. Silakan masukkan uang pas atau lebih.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        txt5.setText("");

        StringBuilder statusTabel = new StringBuilder("Status Pembayaran:\n");
        for (int i = 0; i < model.getRowCount(); i++) {
            Object bulanDiTabel = model.getValueAt(i, 0);
            Object statusDiTabel = model.getValueAt(i, 1);
            if (bulanDiTabel != null && statusDiTabel != null) {
                statusTabel.append(bulanDiTabel.toString()).append(": ").append(statusDiTabel.toString()).append("\n");
            }
        }

        JOptionPane.showMessageDialog(this, statusTabel.toString(), "Status Pembayaran", JOptionPane.INFORMATION_MESSAGE);
    
