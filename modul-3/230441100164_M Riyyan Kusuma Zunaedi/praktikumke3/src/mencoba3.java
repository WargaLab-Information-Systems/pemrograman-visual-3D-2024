    private void btnjumlahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnjumlahActionPerformed
        // TODO add your handling code here:
    if (txt1.getText().trim().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Isi dulu Nama.", "Error", JOptionPane.ERROR_MESSAGE);
    return;
    }

    if (txt2.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Isi dulu Nomor.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    List<String> bulanDipilih = new ArrayList<>();
    if (cbjanuari.isSelected()) bulanDipilih.add("Januari");
    if (cbfebruari.isSelected()) bulanDipilih.add("Februari");
    if (cbmaret.isSelected()) bulanDipilih.add("Maret");
    if (cbapril.isSelected()) bulanDipilih.add("April");
    if (cbmei.isSelected()) bulanDipilih.add("Mei");
    if (cbjuni.isSelected()) bulanDipilih.add("Juni");
    if (cbjuli.isSelected()) bulanDipilih.add("Juli");
    if (cbagustus.isSelected()) bulanDipilih.add("Agustus");
    if (cbseptember.isSelected()) bulanDipilih.add("September");
    if (cboktober.isSelected()) bulanDipilih.add("Oktober");
    if (cbnovember.isSelected()) bulanDipilih.add("November");
    if (cbdesember.isSelected()) bulanDipilih.add("Desember");

    if (bulanDipilih.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Pilih setidaknya satu bulan.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    List<String> bulanSudahDibayar = new ArrayList<>();
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

    for (int i = 0; i < model.getRowCount(); i++) {
        Object bulanDiTabelObj = model.getValueAt(i, 0);
        Object statusDiTabelObj = model.getValueAt(i, 1);
        if (bulanDiTabelObj != null && statusDiTabelObj != null) {
            String bulanDiTabel = bulanDiTabelObj.toString();
            String statusDiTabel = statusDiTabelObj.toString();
            if (statusDiTabel.equals("Sudah Lunas")) {
                bulanSudahDibayar.add(bulanDiTabel);
            }
        }
    }

    int totalBulanBelumDibayar = 0;
    for (String bulan : bulanDipilih) {
        if (!bulanSudahDibayar.contains(bulan)) {
            totalBulanBelumDibayar++;
        }
    }

    int totalSPP = totalBulanBelumDibayar * hargaSPP;
    txt5.setText(String.valueOf(totalSPP)); 

    StringBuilder infoBulan = new StringBuilder("Bulan yang dipilih:\n");
    for (String bulan : bulanDipilih) {
        infoBulan.append(bulan).append("\n");
    }
    infoBulan.append("Total pembayaran: ").append(totalSPP).append(" Rupiah");

    JOptionPane.showMessageDialog(this, infoBulan.toString(), "Informasi Pembayaran", JOptionPane.INFORMATION_MESSAGE);

    txt3.setText("");


    }//GEN-LAST:event_btnjumlahActionPerformed

