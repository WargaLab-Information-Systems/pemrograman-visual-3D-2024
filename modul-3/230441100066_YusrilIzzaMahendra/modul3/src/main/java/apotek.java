    private void gejalaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gejalaActionPerformed
        // TODO add your handling code here:
        String selectedGejala = (String) gejala.getSelectedItem();
    
        rekomobat.setText("");

        switch (selectedGejala) {
            case "Pilih Gejala":
                rekomobat.setText("");
                break;
            case "Demam dan Nyeri":
                rekomobat.setText("Rekomendasi obat:\n- Panadol\n- Proris");
                break;
            case "Infeksi Bakteri dan Alergi":
                rekomobat.setText("Rekomendasi obat:\n- Amoxan\n- Zyrtec");
                break;
            case "Maag dan Diare":
                rekomobat.setText("Rekomendasi obat:\n- Losec\n- Imodium");
                break;
            case "Kolesterol dan Darah Tinggi":
                rekomobat.setText("Rekomendasi obat:\n- Zocor\n- Norvasc");
                break;
            case "Obat Penenang":
                rekomobat.setText("Rekomendasi obat:\n- Valium");
                break;
            case "Kontrol Kadar Gula Darah":
                rekomobat.setText("Rekomendasi obat:\n- Glucophage");
                break;
            default:
                rekomobat.setText("");
                break;
        }
    }//GEN-LAST:event_gejalaActionPerformed

