 private void tampilkanObat(String gejala, String[] namaObat, String[] deskripsiObat) {
        switch (gejala) {
            case "Pusing":
                NamaObat.setText("Nama Obat : " + namaObat[9] + ", " + namaObat[1] + " dan " + namaObat[3]);
                Deskripsi.setText("Deskripsi : " + deskripsiObat[9] + ", " + deskripsiObat[1] + " dan " + deskripsiObat[3]);
                break;
            case "Batuk":
                NamaObat.setText("Nama Obat : " + namaObat[5] + ", " + namaObat[6] + " dan " + namaObat[7]);
                Deskripsi.setText("Deskripsi : " + deskripsiObat[5] + ", " + deskripsiObat[6] + " dan " + deskripsiObat[7]);
                break;
            case "Demam":
                NamaObat.setText("Nama Obat : " + namaObat[0] + " dan " + namaObat[1]);
                Deskripsi.setText("Deskripsi : " + deskripsiObat[0] + " dan " + deskripsiObat[1]);
                break;
            case "Flu":
                NamaObat.setText("Nama Obat : " + namaObat[5] + ", " + namaObat[7] + ", " + namaObat[8] + " dan " + namaObat[9]);
                Deskripsi.setText("Deskripsi : " + deskripsiObat[5] + ", " + deskripsiObat[7] + ", " + deskripsiObat[8] + " dan " + deskripsiObat[9]);
                break;
            case "Nyeri Otot":
                NamaObat.setText("Nama Obat : " + namaObat[0] + ", " + namaObat[1] + " dan " + namaObat[8]);
                Deskripsi.setText("Deskripsi : " + deskripsiObat[0] + ", " + deskripsiObat[1] + " dan " + deskripsiObat[8]);
                break;
            default:
                NamaObat.setText("Nama Obat:... ");
                Deskripsi.setText("Deskripsi:... ");
                break;
        }
    }
    

