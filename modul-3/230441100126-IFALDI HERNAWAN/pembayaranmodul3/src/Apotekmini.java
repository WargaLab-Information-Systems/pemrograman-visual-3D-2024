        // TODO add your handling code here:
        String selectedOptions = "";
        
        String gejalaInput = gejala.getText();
        
        if (antanginjrg.isSelected()) {
            selectedOptions += antanginjrg.getText() + " telah dipilih\n";
        }
        if (antangincairanak.isSelected()) {
            selectedOptions += "Anda telah memilih obat Antangin Cair Anak\n";
        }
        if (obherbal.isSelected()) {
            selectedOptions += "Anda telah memilih obat OB Herbal\n";
        }
        if (tolakangin.isSelected()) {
            selectedOptions += "Anda telah memilih obat Tolak angin\n";
        }
