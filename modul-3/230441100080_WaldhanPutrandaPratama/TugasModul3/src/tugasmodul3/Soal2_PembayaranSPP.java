    private void penghitungan() {
        try {
            int pembayaran = Integer.parseInt(tftotalpembayaran.getText()); // Total harga yang baru
            int uangmasuk = Integer.parseInt(tfbayar.getText()); // Uang yang sudah dimasukkan
            int kembalian = uangmasuk - pembayaran; // Hitung kembalian


            if (kembalian >= 0) {
                tfkembalian.setText(String.valueOf(kembalian)); // Menampilkan kembalian
            } else {
                tfkembalian.setText("Uang Tidak Cukup"); // Jika uang kurang
            }
        } catch (NumberFormatException e) {
            tfkembalian.setText(""); // Kosongkan jika error parsing
        }
    }
