    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
           if (jTextField1.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama tidak boleh kosong! Mohon isi nama dengan benar.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
          }
          if (!januari.isSelected() && !februari.isSelected() && !maret.isSelected() && !april.isSelected() && !mei.isSelected() && !juni.isSelected() && !juli.isSelected() && !agustus.isSelected() && !september.isSelected() && !oktober.isSelected() && !november.isSelected() && !desember.isSelected()) {
                JOptionPane.showMessageDialog(this, "Mohon pilih minimal satu bulan!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
          }
          if (jTextField4.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Mohon masukkan nominal SPP/bulan!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
          }

          int sppPerBulan = Integer.parseInt(jTextField4.getText());
          int jumlahBulan = 0;
                    if (januari.isSelected() && januari.isEnabled()) jumlahBulan++;
                    if (februari.isSelected() && februari.isEnabled()) jumlahBulan++;
                    if (maret.isSelected() && maret.isEnabled()) jumlahBulan++;
                    if (april.isSelected() && april.isEnabled()) jumlahBulan++;
                    if (mei.isSelected() && mei.isEnabled()) jumlahBulan++;
                    if (juni.isSelected() && juni.isEnabled()) jumlahBulan++;
                    if (juli.isSelected() && juli.isEnabled()) jumlahBulan++;
                    if (agustus.isSelected() && agustus.isEnabled()) jumlahBulan++;
                    if (september.isSelected() && september.isEnabled()) jumlahBulan++;
                    if (oktober.isSelected() && oktober.isEnabled()) jumlahBulan++;
                    if (november.isSelected() && november.isEnabled()) jumlahBulan++;
                    if (desember.isSelected() && desember.isEnabled()) jumlahBulan++;
           int jumlahBayar = sppPerBulan * jumlahBulan;
           jTextField3.setText(String.valueOf(jumlahBayar));

    }//GEN-LAST:event_hitungActionPerformed

