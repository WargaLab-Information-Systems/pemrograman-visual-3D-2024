    private void bayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bayarActionPerformed
          if (jTextField5.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Mohon masukkan nominal uang bayar!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                    return;
          }

          int jumlahBayar = Integer.parseInt(jTextField3.getText());
          int bayar = Integer.parseInt(jTextField5.getText());
          if (bayar < jumlahBayar) {
                     JOptionPane.showMessageDialog(this, "Uang bayar kurang dari tagihan! Silakan periksa kembali.");
          } else {
                    int kembalian = bayar - jumlahBayar;
                    jTextField2.setText(String.valueOf(kembalian));
                    JOptionPane.showMessageDialog(this, "Pembayaran berhasil!");
          }
    }//GEN-LAST:event_bayarActionPerformed

