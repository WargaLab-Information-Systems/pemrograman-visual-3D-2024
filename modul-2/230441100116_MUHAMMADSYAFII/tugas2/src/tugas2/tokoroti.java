    private void kurangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kurangActionPerformed
        // TODO add your handling code here:
        int Jumlah = Integer.parseInt(jumlah.getText());
        if (Jumlah <= 0){
            JOptionPane.showMessageDialog(null, "jumlah tidak boleh kurang dari 0","Error",JOptionPane.WARNING_MESSAGE);
        } else {
            Jumlah--;
        }

        jumlah.setText(String.valueOf(Jumlah));
    }//GEN-LAST:event_kurangActionPerformed

    private void totallActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totallActionPerformed
        if(menu.getSelection() == null){
            JOptionPane.showMessageDialog(null, "Mohon pilih salah satu menu terlebih dahulu!");
        } else if (topping.getSelection() == null){
            JOptionPane.showMessageDialog(null, "Mohon pilih salah satu topping terlebih dahulu!");
        }else {
            int Jumlah = Integer.parseInt(jumlah.getText());

            int Menu = 0;
            if (ba.isSelected()){
                Menu = 20000;
            } else if (bf.isSelected()){
                Menu = 50000;
            } else if (bg.isSelected()){
                Menu = 55000;
            } else if (muffin.isSelected()){
                Menu = 35000;
            } else if (chiffon.isSelected()){
                Menu = 40000;
            } else if (cromboloni.isSelected()){
                Menu = 75000;
            }

            int Topping = 0;
            if (coklat.isSelected()){
                Topping = 5000;
            } else if (keju.isSelected()){
                Topping = 8500;
            } else if (vanilla.isSelected()){
                Topping = 10000;
            }

            int Total = (Menu + Topping) * Jumlah;

            if (Total > 100000) {
                Total *= 0.95;
                JOptionPane.showMessageDialog(null, "Karena pembelian anda lebih dari Rp. 100.000\nanda mendapatkan diskon sebanyak 5%");
            }

            total.setText(String.valueOf(Total));
            tambah.setEnabled(false);
            kurang.setEnabled(false);
        }
    }//GEN-LAST:event_totallActionPerformed

