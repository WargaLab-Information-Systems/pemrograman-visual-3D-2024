    private void bayar() {
        try {
            int Cash = Integer.parseInt(cash.getText());
            int Total = Integer.parseInt(total.getText());
            int kembalian = Cash - Total;

            if (Cash < Total) {
                kembali.setText("Uang Tidak Cukup");
            } else {
               
                kembali.setText(String.valueOf("Rp. "+kembalian));
            }
        } catch (NumberFormatException e) {
            kembali.setText("Input Tidak Sesuai");
        }
    }
    
    
