    private void totalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalButtonActionPerformed
        // TODO add your handling code here:
        if (buttonGroup1.getSelection() == null || buttonGroup2.getSelection() == null) {
           JOptionPane.showMessageDialog(this, "Mohon pilih salah satu menu terlebih dahulu!",
                   "Warning", JOptionPane.WARNING_MESSAGE);
           return;
        }

        int hargaRoti = getHargaRotiDipilih();
        int hargaTopping = getHargaToppingDipilih();
        int jumlah = Integer.parseInt(jumlahTF.getText());
        int totalHarga = (hargaRoti + hargaTopping) * jumlah;

        if (totalHarga > 100000) {
            int diskon = totalHarga * 5/100 ;
            totalHarga -= diskon;
        }

        totalTF.setText(String.valueOf(totalHarga));
    }                                        

    // Mendapatkan harga roti yang dipilih
    private int getHargaRotiDipilih() {
        if (roti1.isSelected()) return 12000;
        if (roti2.isSelected()) return 15000;
        if (roti3.isSelected()) return 15000;
        if (roti4.isSelected()) return 20000;
        if (roti5.isSelected()) return 20000;
        if (roti6.isSelected()) return 20000;
        return 0;
    }

    // Mendapatkan harga topping yang dipilih
    private int getHargaToppingDipilih() {
        if (topping1.isSelected()) return 0;
        if (topping2.isSelected()) return 5000;
        if (topping3.isSelected()) return 5000;
        if (topping4.isSelected()) return 5000;
        return 0;
    }//GEN-LAST:event_totalButtonActionPerformed

