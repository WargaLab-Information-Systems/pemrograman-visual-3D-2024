    private void cashTFKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cashTFKeyReleased
        // TODO add your handling code here:
        try {
            // Ambil nilai dari totalTF dan cashTF
            int total = Integer.parseInt(totalTF.getText());
            int cash = Integer.parseInt(cashTF.getText());

            // Jika uang tunai lebih besar atau sama dengan total, hitung kembalian
            if (cash >= total) {
                int kembalian = cash - total;
                kembalianTF.setText(String.valueOf(kembalian));  // Tampilkan kembalian di kembalianTF
            } else {
                // Jika uang tidak cukup, tampilkan pesan
                kembalianTF.setText("Uang tidak cukup");
            }
        } catch (NumberFormatException ex) {
            // Jika input bukan angka yang valid, tampilkan pesan kesalahan
            kembalianTF.setText("Input tidak valid");
        }
    }//GEN-LAST:event_cashTFKeyReleased

