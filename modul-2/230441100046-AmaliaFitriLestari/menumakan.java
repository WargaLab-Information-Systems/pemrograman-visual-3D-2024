    private void cashKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cashKeyReleased
        // TODO add your handling code here:
            kembali.setEditable(false);
            try {
                double totalharga = Double.parseDouble(totalakhir.getText()); 
                double cash2 = Double.parseDouble(cash.getText());
                double kembalian = cash2 - totalharga ;
                if (cash2 >= totalharga){
                    kembali.setText(String.valueOf(kembalian));
                }else {
                    kembali.setText("uang belum cukup");
                 }
            } catch(NumberFormatException e){
                kembali.setText("input tidak benar");
        } 
    }//GEN-LAST:event_cashKeyReleased

