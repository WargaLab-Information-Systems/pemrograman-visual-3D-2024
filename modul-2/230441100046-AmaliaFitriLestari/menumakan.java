    private void TotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TotalActionPerformed
        // TODO add your handling code here:
        total.setEditable(false);  
        double hargaroti = 0;
        double hargatopping = 0;
        double diskon = 0 ;
        double totaldiskon = 0;
      
        if (buttonGroup1.getSelection()== null){
          JOptionPane.showMessageDialog(null, "harap isi menu roti!", "error", JOptionPane.ERROR_MESSAGE);
        }
        if (buttonGroup2.getSelection()  == null){
          JOptionPane.showMessageDialog(null, "harap isi topping roti!", "error", JOptionPane.ERROR_MESSAGE);
        }
        if (pilihan1.isSelected()){
            hargaroti = 20000;
        } else if (pilihan2.isSelected()){
            hargaroti = 25000;
        } else if (pilihan3.isSelected()){
            hargaroti = 17000;
        } else if (pilihan4.isSelected()){
            hargaroti = 23000;    
        } else if (pilihan5.isSelected()){
            hargaroti = 13000;
        } else if (pilihan6.isSelected()){
            hargaroti = 17000;
        }

        if (notopping.isSelected()){
            hargatopping = 0;
        } else if (kacang.isSelected()){
            hargatopping = 3000;
        } else if (keju.isSelected()){
            hargatopping = 5000;
        } else if (mesis.isSelected()){
            hargatopping = 4000;    
        }       

        double total1 = hargaroti + hargatopping ;
        int jumlah = Integer.parseInt(samadengan.getText()); 
        double totalharga2 = total1 * jumlah ;
       
        if (totalharga2 >= 100000){
            diskon = 5; 
        } else {
            diskon = 0;
        }
        totaldiskon = totalharga2 * (diskon / 100);
        double totalharga = totalharga2 - totaldiskon ;
//        total.setText(String.valueOf(totalharga));
        total.setText("Hotal harga asli :   " +totalharga2 + 
                "\nDiskon :  " + diskon +
                "\nTotal diskon :  " + totaldiskon
        );
        totalakhir.setText(String.valueOf(totalharga));
    }//GEN-LAST:event_TotalActionPerformed

