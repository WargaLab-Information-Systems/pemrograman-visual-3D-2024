    private void txt3KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt3KeyReleased
        // TODO add your handling code here:                               
    try {
    String input = txt3.getText();
    if (!input.matches("\\d*")) {  
        JOptionPane.showMessageDialog(this, "Masukkan jumlah uang yang valid (hanya angka)", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    if (!input.isEmpty()) {
        int uangDibayarkan = Integer.parseInt(input);
        
        int kembalian = uangDibayarkan - total;
      
        if (kembalian < 0) {
            kembalian = 0;
        }
        txt4.setText(String.valueOf(kembalian));
        }
        } catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Masukkan jumlah uang yang valid", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_txt3KeyReleased

