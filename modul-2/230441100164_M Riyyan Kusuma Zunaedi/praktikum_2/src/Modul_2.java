    private void btntotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntotalActionPerformed
       
       if (hargaRoti == 0) {
        JOptionPane.showMessageDialog(this, "Pilih salah satu jenis roti terlebih dahulu", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }
        if (rdbtntanpatopping.isSelected()) {
        hargaTopping = 0; 
        } else if (hargaTopping == 0) {
    
        JOptionPane.showMessageDialog(this, "Pilih salah satu jenis topping terlebih dahulu", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }
        if (jumlah == 0) {
        JOptionPane.showMessageDialog(this, "Masukkan jumlah yang ingin dibeli", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }
        total = jumlah * (hargaRoti + hargaTopping);
        if (total > 100000) {
        double diskon = total * 0.05; 
        total -= diskon; 
        if (txt3.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Anda mendapatkan diskon 5% dengan total: " + total, "Diskon", JOptionPane.INFORMATION_MESSAGE);
        }
    }


    txt2.setText(String.valueOf(total)); 
    
    ImageIcon icon = new javax.swing.ImageIcon(getClass().getResource("/arik jpg 2 (2).jpg")); 


        if (txt3.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan jumlah uang yang dibayarkan", "Error", JOptionPane.ERROR_MESSAGE,icon);
        } else if (!txt3.getText().matches("\\d+")) {
        JOptionPane.showMessageDialog(this, "Masukkan jumlah uang yang valid (hanya angka)", "Error", JOptionPane.ERROR_MESSAGE);
        } else {

        int uangDibayarkan = Integer.parseInt(txt3.getText());

    
        int kembalian = uangDibayarkan - total;

   
        if (kembalian < 0) {
        JOptionPane.showMessageDialog(this, "Uang yang dibayarkan kurang", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
       
        txt4.setText(String.valueOf(kembalian));
    }
    }
    }//GEN-LAST:event_btntotalActionPerformed

