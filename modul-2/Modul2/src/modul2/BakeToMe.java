    private void cashActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cashActionPerformed
       // Pastikan kedua field (tftotal dan tfcash) tidak kosong
    if (!total.getText().isEmpty() && !cash.getText().isEmpty()) {
        int totalHarga = Integer.parseInt(total.getText());
        int bayar = Integer.parseInt(cash.getText());
        
        if (bayar >= totalHarga) {
            int kembalian = bayar - totalHarga;
            kembali.setText(String.valueOf(kembalian));
        } else {
           JOptionPane.showMessageDialog(null, "Uang tidak cukup!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            kembali.setText("");
            kembali.setEditable(false);
        }
    }
    }//GEN-LAST:event_cashActionPerformed

