    private void TOTALActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TOTALActionPerformed
    int totalPrice = 0;
        //MENU
        if (focaccia.isSelected()) {
            totalPrice += 40000;
        } else if (garlic.isSelected()) {
            totalPrice += 25000;
        } else if (redbeans.isSelected()) {
            totalPrice += 35000;
        } else if (brown.isSelected()) {
            totalPrice += 40000;
        } else if (stick.isSelected()) {
            totalPrice += 18000;
        } else if (crumpet.isSelected()) {
            totalPrice += 21000;
        }
        if (!focaccia.isSelected() && !garlic.isSelected() && !redbeans.isSelected() && !brown.isSelected() && !stick.isSelected() && !crumpet.isSelected()) {
        JOptionPane.showMessageDialog(null, "Silakan pilih menu terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
        }
        
        //TOPPINGS
        if (chocolate.isSelected()) {
            totalPrice += 5000;
        } else if (cheese.isSelected()) {
            totalPrice += 6000;
        } else if (peanut.isSelected()) {
            totalPrice += 4000;
        }
        if (!chocolate.isSelected() && !cheese.isSelected() && !peanut.isSelected() && !notopings.isSelected()) {
            JOptionPane.showMessageDialog(null, "Silakan pilih toping terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        //JUMLAH 
         if (jumlah.getText().isEmpty() || jumlah.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "Silakan masukkan jumlah terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
            }
         //TOTAL 
        int jumlahBeli = Integer.parseInt(jumlah.getText());
        totalPrice *= jumlahBeli;
        // DISKON
        int hargaSebelumDiskon = totalPrice;
        if (totalPrice > 100000) {
            totalPrice = (int) (totalPrice * 0.95); // 5% discount
        }
        String infoHarga = "Harga sebelum diskon: Rp" + hargaSebelumDiskon + "\n";
        if (hargaSebelumDiskon != totalPrice) {
            infoHarga += "Diskon: 5% (Rp" + (hargaSebelumDiskon - totalPrice) + ")\n";
        }
        infoHarga += "Total harga: Rp" + totalPrice;
        TA.setText(infoHarga);
        TA.setEditable(false);
        
        total.setText(String.valueOf(totalPrice));
        total.setEditable(false);
    }//GEN-LAST:event_TOTALActionPerformed

