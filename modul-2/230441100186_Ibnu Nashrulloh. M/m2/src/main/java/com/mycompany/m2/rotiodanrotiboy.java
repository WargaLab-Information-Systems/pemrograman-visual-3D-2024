private void penghitungan() {
        try {
            double uangmasuk = Double.parseDouble(uangcash.getText());
            double kembaliann = uangmasuk - totalHarga;
            if (kembaliann >= 0) {
                kembalian.setText(String.valueOf(kembaliann));
            } else {
                kembalian.setText("Uang Tidak Cukup");
            }
        } catch (NumberFormatException e) {
            kembalian.setText("Input Tidak Sesuai");
        }
}

