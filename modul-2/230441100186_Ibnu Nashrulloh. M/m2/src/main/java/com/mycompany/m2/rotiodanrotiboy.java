    private void hitungtotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungtotalActionPerformed
        // TODO add your handling code here:
        if (buttonGroup1.getSelection() == null || buttonGroup2.getSelection() == null) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Silakan pilih menu roti dan topping terlebih dahulu!",
            "Peringatan",
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
        return;
        }
         double menu = 0;
    if (RotiO.isSelected()) menu = 10000;
    else if (Rotiboy.isSelected()) menu = 13000;
    else if (Cromboloni.isSelected()) menu = 18000;
    else if (RotiKopi.isSelected()) menu = 18000;
    else if (RotiCanai.isSelected()) menu = 16000;
    else if (Rotitisu.isSelected()) menu = 25000;
    
    double topping = 0;
    if (strawberry.isSelected()) topping = 6000; 
    else if (keju.isSelected()) topping = 5000; 
    else if (cokelat.isSelected()) topping = 5000; 
    else if (notopping.isSelected()) topping = 0; 
    totalHarga = jumlahPesanan*(menu + topping);
    if (totalHarga >= 100000) {
        totalHarga = totalHarga * 0.95; 
    }
    
    hasiltotal.setText(String.format("Rp %.2f", totalHarga));
     

    }//GEN-LAST:event_hitungtotalActionPerformed

