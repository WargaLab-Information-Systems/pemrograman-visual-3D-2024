    private void hitungKembalian() {
    try {
        if (!atotal.getText().isEmpty() && !acash.getText().isEmpty()) {
            double total = Double.parseDouble(atotal.getText().replace("Rp", "").trim());
            double cash = Double.parseDouble(acash.getText().trim());

            double kembali = cash - total;

            if (kembali < 0) {
                akembali.setText("Kurang");
            } else {
                akembali.setText(String.format("Rp%.0f", kembali));
            }
        } else {
            akembali.setText("");
        }
    } catch (NumberFormatException e) {
        akembali.setText("");
    }
    }
