    private void jtotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtotalActionPerformed
    double hargaRoti = 0, hargaTopping = 0;

    String jumlahStr = ajumlah.getText().trim();
    if (jumlahStr.isEmpty() || jumlahStr.equals("0")) {
        JOptionPane.showMessageDialog(this, "Tambahkan dulu jumlahnya.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    int jumlah = Integer.parseInt(jumlahStr);

    if (!(rdmenu1.isSelected() || rdmenu2.isSelected() || rdmenu3.isSelected() || 
          rdmenu4.isSelected() || rdmenu5.isSelected() || rdmenu6.isSelected() || 
          rdmenu7.isSelected() || rdmenu8.isSelected())) {
        JOptionPane.showMessageDialog(this, "Silakan pilih salah satu menu roti.", "Error", JOptionPane.ERROR_MESSAGE);
        return; 
    }

    if (!(jtoping1.isSelected() || jtoping2.isSelected() || jselai1.isSelected() || jselai2.isSelected() || jtanpa.isSelected())) {
        JOptionPane.showMessageDialog(this, "Silakan pilih salah satu topping atau 'Tanpa Topping'.", "Error", JOptionPane.ERROR_MESSAGE);
        return;  
    }

    if (rdmenu1.isSelected()) {
        hargaRoti = 23000;
    } else if (rdmenu2.isSelected()) {
        hargaRoti = 18000;
    } else if (rdmenu3.isSelected()) {
        hargaRoti = 14000;
    } else if (rdmenu4.isSelected()) {
        hargaRoti = 39000;
    } else if (rdmenu5.isSelected()) {
        hargaRoti = 45000;
    } else if (rdmenu6.isSelected()) {
        hargaRoti = 17000;
    } else if (rdmenu7.isSelected()) {
        hargaRoti = 20000;
    } else if (rdmenu8.isSelected()) {
        hargaRoti = 25000;
    }

    if (jtoping1.isSelected()) {
        hargaTopping = 2000;
    } else if (jtoping2.isSelected()) {
        hargaTopping = 7000;
    } else if (jselai1.isSelected()) {
        hargaTopping = 10000;
    } else if (jselai2.isSelected()) {
        hargaTopping = 10000;
    } else if (jtanpa.isSelected()) {
        hargaTopping = 0;  
    }

    double totalTanpaDiskon = (hargaRoti + hargaTopping) * jumlah;

    double totalDenganDiskon = totalTanpaDiskon;
    if (totalTanpaDiskon > 100000) {
        totalDenganDiskon = totalTanpaDiskon * 0.95;
    }
    
    if (totalTanpaDiskon > 100000) {
        atanpa.setText(String.format("Rp%.0f", totalTanpaDiskon));
    } 
    else {
        atanpa.setText("Belum dapat diskon");
    }

    atotal.setText(String.format("Rp%.0f", totalDenganDiskon));
    hitungKembalian();
    }//GEN-LAST:event_jtotalActionPerformed

