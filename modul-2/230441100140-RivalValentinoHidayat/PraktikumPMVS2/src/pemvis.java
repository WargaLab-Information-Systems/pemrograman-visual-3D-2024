    private void hitungKembalianOtomatis() {
    String totalText = jTextFieldTotal.getText();
    String cashText = jTextFieldCash.getText();

    if (!totalText.isEmpty() && !cashText.isEmpty()) {
        int total = Integer.parseInt(totalText); 
        int cash = Integer.parseInt(cashText); 

        int kembalian = cash - total; 

        if (kembalian < 0) {
            jTextFieldKembalian.setText("Uang tidak cukup!"); 
        } else {
            jTextFieldKembalian.setText(String.valueOf(kembalian)); 
        }
    } else {
        jTextFieldKembalian.setText(""); 
    }
}
    
