    private void jButtonTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTotalActionPerformed
        // TODO add your handling code here:

    int hargaRoti = 0;

    if (jRadioButtonRPolos.isSelected()) {
        hargaRoti = 20000;
    } else if (jRadioButtonRGandum.isSelected()) {
        hargaRoti = 21000;
    } else if (jRadioButtonRGula.isSelected()) {
        hargaRoti = 23000;
    } else if (jRadioButtonRCaramel.isSelected()) {
        hargaRoti = 25000;
    } else if (jRadioButtonRCream.isSelected()) {
        hargaRoti = 24000;
    } else if (jRadioButtonREsKrim.isSelected()) {
        hargaRoti = 27000;
    } else if (jRadioButtonRDaging.isSelected()) {
        hargaRoti = 27000;
    } else if (jRadioButtonRTelur.isSelected()) {
        hargaRoti = 25000;
    } else if (jRadioButtonRBakar.isSelected()) {
        hargaRoti = 23000;
    } else {
        JOptionPane.showMessageDialog(null, "Pilih roti terlebih dahulu!");
        return;
    }

    int hargaTopping = 0;
    if (jRadioButtonSausKacang.isSelected()) {
        hargaTopping += 1000;
    } else if  (jRadioButtonKeju.isSelected()) {
        hargaTopping += 1500;
    } else if (jRadioButtonCoklat.isSelected()) {
        hargaTopping += 2000;   
    }

   
     String jumlahText = jTextFieldJumlah.getText();
     if (jumlahText.isEmpty()) {
     JOptionPane.showMessageDialog(null, "Masukkan jumlah roti terlebih dahulu!");
     return;
    }
     int jumlah = Integer.parseInt(jumlahText); 
     int total = (hargaRoti + hargaTopping) * jumlah;
     if (total > 100000) {
         total -= (total * 5 / 100);
      }
    
     jTextFieldTotal.setText(String.valueOf(total));

     if (!jTextFieldCash.getText().isEmpty()) {
     int cash = Integer.parseInt(jTextFieldCash.getText());
     int kembalian = cash - total;

     if (kembalian < 0) {
        JOptionPane.showMessageDialog(null, "Uang tidak cukup!");
    } else {
        jTextFieldKembalian.setText(String.valueOf(kembalian));
      }
     }
    }//GEN-LAST:event_jButtonTotalActionPerformed

