private void penghitungan() {
        try {
          
            double uangmasuk = Double.parseDouble(Cash.getText());
            double kembaliann2 = uangmasuk - TotalHarga22;

            if (kembaliann2 >= 0) {
                Kembali.setText(String.valueOf(kembaliann2));
            } else {
                Kembali.setText("Uang Tidak Cukup");
            }
        } catch (NumberFormatException e) {
            Kembali.setText("Input Tidak Sesuai");
        }
}
        
