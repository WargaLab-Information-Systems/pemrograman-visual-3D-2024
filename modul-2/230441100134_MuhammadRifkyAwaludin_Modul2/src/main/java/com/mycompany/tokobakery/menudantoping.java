    private void BtnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTotalActionPerformed
        // TODO add your handling code here:
        if (BtnGrpMenu.getSelection() == null || BtnGrpToping.getSelection() == null) {
        javax.swing.JOptionPane.showMessageDialog(this,
                "Mohon Memilih Salah Satu Roti dan Toping Terlebih Dahulu!!",
                "Peringatan!",
                javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Harga Menu
    double HargaMenu = 0;
    if (Bagel.isSelected()) HargaMenu = 10000;
    else if (Brownis.isSelected()) HargaMenu = 15000; 
    else if (Croissant.isSelected()) HargaMenu = 10000;
    else if (Cubit.isSelected()) HargaMenu = 12000;
    else if (Focaccia.isSelected()) HargaMenu = 20000;
    else if (Muffin.isSelected()) HargaMenu = 12000;
    else if (Pancake.isSelected()) HargaMenu = 25000;
    else if (RotiBakar.isSelected()) HargaMenu = 25000;
    
    // Harga Toping
    double HargaToping = 0;
    if (Mete.isSelected()) HargaToping = 5000;
    else if (Keju.isSelected()) HargaToping = 5000;
    else if (Coklat.isSelected()) HargaToping = 4000;
    else if (Strawbery.isSelected()) HargaToping = 3000;
    else if (Kacang.isSelected()) HargaToping = 3000;
    else if (Anggur.isSelected()) HargaToping = 3000;
    else if (Jeruk.isSelected()) HargaToping = 3000;
    else if (Original.isSelected()) HargaToping = 0;
    
    // Perhitungan Total Harga
    TotalHarga22 = (HargaMenu + HargaToping) * JumlahPesanan;
    
    // Diskon
    if (TotalHarga22 >= 100000) {
        double discount = TotalHarga22 * 0.05;
        TotalHarga22 = TotalHarga22 * 0.95;
        JOptionPane.showMessageDialog(null, "Anda Mendapatkan Diskon Sebesar " + discount);
    }
    
    // Tampilkan Total Harga
    TotalHarga.setText(String.format("Rp %.2f", TotalHarga22));
       
    }//GEN-LAST:event_BtnTotalActionPerformed

