    private void btntotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntotalActionPerformed
        // TODO add your handling code here:
        if (hargaIce == 0){
            JOptionPane.showMessageDialog(this, "pilih salah satu jenis ice cream terlebih dahulu", "error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        total = jumlah * (hargaIce + hargaTopping);
        if (total > 100000) {
            double diskon = total * 0.05; // 5% diskon
            total -= diskon; // Kurangi total dengan diskon

            ImageIcon icon = new ImageIcon("C:/Users/ahmad/Downloads/discount.png");

            JOptionPane.showMessageDialog(this, "Anda mendapatkan diskon 5%!", "Diskon", JOptionPane.INFORMATION_MESSAGE,icon);
        }
        txt2.setText(String.valueOf(total));

        int uangDibayarkan = Integer.parseInt(txt3.getText());
        int kembalian = uangDibayarkan - total;

        if (kembalian < 0 ){
            JOptionPane.showMessageDialog(this, "uang yang di bayarkan kurang", "error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        txt4.setText(String.valueOf(kembalian));    
    }//GEN-LAST:event_btntotalActionPerformed

