    private void penghitungan() {
        try {
            double hargaafterdiskon = Double.parseDouble(total.getText());
            double uangmasuk = Double.parseDouble(bayar.getText());
            double kembalian = uangmasuk - hargaafterdiskon;

            if (kembalian >= 0) {
                kembali.setText(String.valueOf(kembalian));
            } else {
                kembali.setText("Uang Tidak Cukup");
            }
        } catch (NumberFormatException e) {
            kembali.setText("Bayar yang bener");
        }
    }
