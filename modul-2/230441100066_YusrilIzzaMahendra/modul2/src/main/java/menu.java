    private void cetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cetakActionPerformed
        // TODO add your handling code here:
        if (!sudahtotal) {
            JOptionPane.showMessageDialog(null, "Anda harus menghitung total harga terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (buttonGroup1.getSelection() == null) {
            JOptionPane.showMessageDialog(null, "Anda harus memilih menu terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (buttonGroup2.getSelection() == null) {
            JOptionPane.showMessageDialog(null, "Anda harus memilih topping terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (Integer.parseInt(jumlah.getText()) <= 0) {
            JOptionPane.showMessageDialog(null, "Jumlah pesanan harus lebih dari 0!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (bayar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Anda harus mengisi jumlah pembayaran!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String menupilih = "";
        if (rengoku.isSelected()) {
            menupilih = "Donat Rengoku";
        } else if (ace.isSelected()) {
            menupilih = "Donat Ace";
        } else if (minato.isSelected()) {
            menupilih = "Donat Minato";
        } else if (kakyoin.isSelected()) {
            menupilih = "Donat Kakyoin";
        } else if (abbachio.isSelected()) {
            menupilih = "Donat Abbachio";
        } else if (genos.isSelected()) {
            menupilih = "Donat Genos";
        } else if (issei.isSelected()) {
            menupilih = "Donat Issei";
        } else if (ichigo.isSelected()) {
            menupilih = "Donat Ichigo";
        }

        String toppingpilih = "";
        if (haki.isSelected()) {
            toppingpilih = "Haki(Chocolate)";
        } else if (chakra.isSelected()) {
            toppingpilih = "Chakra(Bluberry)";
        } else if (nen.isSelected()) {
            toppingpilih = "Nen(Vanilla)";
        } else if (stands.isSelected()) {
            toppingpilih = "Stands(nut)";
        }

        String hargaawal = total.getText();
        String uangbayar = bayar.getText();
        String kembalian = kembali.getText();
        String jumlahpesanan = jumlah.getText();

        double hargasemula = Double.parseDouble(hargaawal);
        double diskon = 0;
        if (hargasemula >= 100000) {
            diskon = hargasemula * 0.05;
        }
        String diskonnya = String.valueOf(diskon);

        String hasilcetak = "Menu                   : " + menupilih + "\n" +
                            "Topping                : " + toppingpilih + "\n" +
                            "Jumlah Pesanan : " + jumlahpesanan + "\n" +
                            "Harga Awal         : Rp " + hargaawal + "\n" +
                            "Diskon                 : Rp " + diskonnya + "\n" +
                            "Total Harga        : Rp " + total.getText() + "\n" +
                            "Uang Dibayar   : Rp " + uangbayar + "\n" +
                            "Kembalian      : Rp " + kembalian;

        JOptionPane.showMessageDialog(null, hasilcetak, "STRUK PEMESANAN", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_cetakActionPerformed

