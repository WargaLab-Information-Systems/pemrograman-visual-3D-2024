    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        if (Data.isSelected()) {
            Tugas2 Tugas2 = new Tugas2();
            Tugas2.NamaLengkap.setText(NamaLengkap.getText());
            Tugas2.NamaPanggilan.setText(NamaPanggilan.getText());
            Tugas2.TempatLahir.setText(TempatLahir.getText());
            Tugas2.TanggalLahir.setText(TanggalLahir.getText());
            Tugas2.Email.setText(Email.getText());
            Tugas2.Nomerhp.setText(Nomerhp.getText());
            String Lomba = (String)L.getSelectedItem().toString();
            Tugas2.Lomba.setText(Lomba);

            String JenisKelamin = "";
            if (Laki.isSelected()) {
                JenisKelamin = "Laki - Laki";
            }else if (Perempuan.isSelected()){
                JenisKelamin = "Perempuan";
            }
            Tugas2.JenisKelamin.setText(JenisKelamin);

            Tugas2.setVisible(true);
            this.dispose();
            
        } else {
            // Menampilkan pesan peringatan jika checkbox belum dicentang
            JOptionPane.showMessageDialog(null, "Harap Checklist Terlebih Dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);

            // Validasi jika ada field yang kosong
            if (NamaLengkap.getText().trim().isEmpty() ||
                NamaPanggilan.getText().trim().isEmpty() ||
                TempatLahir.getText().trim().isEmpty() ||
                TanggalLahir.getText().trim().isEmpty() ||
                Email.getText().trim().isEmpty() ||
                Nomerhp.getText().trim().isEmpty() ||
                !Laki.isSelected() && !Perempuan.isSelected()
                    ){
                Data.setText("(isi form harus lengkap!!!)");
            } else {
                Data.setText(null);
            }
        }
    }//GEN-LAST:event_SimpanActionPerformed

