    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        UseHasil lol = new UseHasil();
        lol.namaL2.setText(namaL.getText());
        lol.namaJ2.setText(namaJ.getText());
        lol.tgl2.setText(tgl.getText());
        if (laki.isSelected()){
            lol.kelamin2.setText("Laki-Laki");
        }
         if (perem.isSelected()){
            lol.kelamin2.setText("Perempuan");
        }
        lol.kota2.setText (kota.getText());
        lol.telepon2.setText(telepon.getText());
        lol.email2.setText(email.getText());
        lol.pendidikan2.setText(pendidikan.getText());
        lol.kategori2.setText(kategori.getSelectedItem().toString());
        
        if (!konfirmasi.isSelected()) {
           JOptionPane.showMessageDialog(null, "Harap mengkonfirmasi bahwa data anda sudah valid", "WARNING", JOptionPane.WARNING_MESSAGE);
        }else{
            if(namaL.getText().isEmpty() 
                ||namaJ.getText().isEmpty()
                ||tgl.getText(). isEmpty() 
                ||buttonGroup1.getSelection() == null
                ||kota.getText(). isEmpty() 
                ||telepon.getText(). isEmpty()
                ||email.getText(). isEmpty()         
                ||pendidikan.getText(). isEmpty()
                ){
                JOptionPane.showMessageDialog(null, "Data tidak Lengkap, Harap Mengisi Keseluruhan data.", "WARNING", JOptionPane.WARNING_MESSAGE);
            }else{
                lol.setVisible(true);
                        dispose();
            }
           
        }
    }//GEN-LAST:event_simpanActionPerformed

