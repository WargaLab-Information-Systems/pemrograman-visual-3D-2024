    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here
        if (!jCheckBox1.isSelected()){
            JOptionPane.showMessageDialog(this, "Anda harus memastikan semua data sudah benar dengan mencentang kotak.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String namaInput = nama.getText();
        String nisnInput = nisn.getText();
        String tempatInput = tempat.getText();
        String emailInput = email.getText();
        String noHpInput = no.getText();
        String alamatInput = alamat.getText();
        String jurusanInput =(String)jurusan.getSelectedItem();
        
        String genderInput="";
        if (laki.isSelected()) {
            genderInput = "Laki-laki";
        } else if (perempuan.isSelected()) {
            genderInput = "Perempuan";
        }

        
        data sp = new data(namaInput, nisnInput, tempatInput, emailInput, noHpInput, alamatInput,jurusanInput, genderInput);
        sp.setVisible(true); //untuk menyembunyikan
        sp.pack(); //menyesuaikan ukuran
        sp.setLocationRelativeTo(null); //ditengah
        this.dispose(); //untuk menutup
    }//GEN-LAST:event_simpanActionPerformed

