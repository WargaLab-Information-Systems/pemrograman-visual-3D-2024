    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
    // TODO add your handling code here:
    String nama = jTextFieldNama.getText();
    String tempatLahir = jTextFieldTempatLahir.getText();
    String tanggalLahir = jTextFieldTanggalLahir.getText();
    String alamat = jTextFieldAlamat.getText();
    String noTelp = jTextFieldNoTelp.getText();
    String email = jTextFieldEmail.getText();
    String prodi = jTextFieldProdi.getText();
    String fakultas = jComboBox1.getSelectedItem().toString();
    
    String jenisKelamin = "";
    if (jRadioButtonLaki.isSelected()) {
        jenisKelamin = "Laki-Laki";
    } else if (jRadioButtonPerempuan.isSelected()) {
        jenisKelamin = "Perempuan";
    }
    
    if (jenisKelamin.equals("")) {
        JOptionPane.showMessageDialog(this, "Harap pilih jenis kelamin.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return; 
    }
    
   
    if (!jCheckBox1.isSelected()) {
    JOptionPane.showMessageDialog(this, "Harap centang kotak persetujuan sebelum menyimpan.", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;   
    }
        
    DataFrame dataFrame = new DataFrame(nama, tempatLahir, tanggalLahir, jenisKelamin, alamat, noTelp, email, prodi, fakultas);  
    dataFrame.setVisible(true);       
    }//GEN-LAST:event_jButtonSimpanActionPerformed

