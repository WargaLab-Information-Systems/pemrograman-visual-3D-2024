    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        if (!datasudah.isSelected()) {
    javax.swing.JOptionPane.showMessageDialog(this, "Silahkan Dicentang Terlebih Dahulu");
    return;
} 

  
    if (tfnama.getText().isEmpty() || tfttl.getText().isEmpty() || 
    tfalamat.getText().isEmpty() || tfnotelp.getText().isEmpty() || 
    tfemail.getText().isEmpty() || tfasalsekolah.getText().isEmpty() || 
    (!lakilaki.isSelected() && !wanita.isSelected())) {

    javax.swing.JOptionPane.showMessageDialog(this, "Silahkan lengkapi data terlebih dahulu");
    return;
} 

   
    NAMA = tfnama.getText();
    Ttl = tfttl.getText();
    ALAMAT = tfalamat.getText();
    NOTELP = tfnotelp.getText();
    EMAIL = tfemail.getText();
    ASALSEKOLAH = tfasalsekolah.getText();

    if (lakilaki.isSelected()) {
    JENISKELAMIN = "lakilaki";
    } else if (wanita.isSelected()) {
    JENISKELAMIN = "wanita";
    }

    AGAMA = agama1.getSelectedItem().toString();

    halaman2 A = new halaman2(NAMA, Ttl, ALAMAT, NOTELP, EMAIL, ASALSEKOLAH, JENISKELAMIN, AGAMA);
    A.setVisible(true);
   

  
    }//GEN-LAST:event_btnsimpanActionPerformed

