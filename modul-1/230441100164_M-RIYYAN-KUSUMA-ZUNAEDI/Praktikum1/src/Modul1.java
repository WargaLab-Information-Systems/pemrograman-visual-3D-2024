    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
          if (!chcksudah.isSelected()) {
        javax.swing.JOptionPane.showMessageDialog(this,
                "Harap centang 'apakah data sudah benar' sebelum melanjutkan.");
    } else {
        if (txt1.getText().isEmpty() || txt2.getText().isEmpty() || txt3.getText().isEmpty() ||
            txt4.getText().isEmpty() || txt5.getText().isEmpty() || txt7.getText().isEmpty()) {
            
            javax.swing.JOptionPane.showMessageDialog(this, 
                    "Penuhi dulu semua data sebelum melanjutkan!", 
                    "Input Tidak Lengkap", 
                    javax.swing.JOptionPane.WARNING_MESSAGE);
 
        } else if (!rdbtnlaki.isSelected() && !rdbtnperempuan.isSelected()) {
                javax.swing.JOptionPane.showMessageDialog(this, 
                "Pilih jenis kelamin sebelum melanjutkan!", 
                "Input Tidak Lengkap", 
                javax.swing.JOptionPane.WARNING_MESSAGE);

  
        } else if (cmbprodi.getSelectedIndex() == -1) { 
                javax.swing.JOptionPane.showMessageDialog(this, 
                "Pilih program studi sebelum melanjutkan!", 
                "Input Tidak Lengkap", 
                javax.swing.JOptionPane.WARNING_MESSAGE);
        } else {
            String Nama = txt1.getText();
            String TTL = txt2.getText();
            String JenisKelamin = "";
            if (rdbtnlaki.isSelected()) {
                JenisKelamin = "laki-laki";
            } else if (rdbtnperempuan.isSelected()) {
                JenisKelamin = "perempuan";
            }
            String Alamat = txt3.getText();
            String NoTelp = txt4.getText();
            String Email = txt5.getText();
            String Prodi = cmbprodi.getSelectedItem().toString();
            String Fakultas = txt7.getText();
            
            lanjutan1 b = new lanjutan1(Nama, TTL, JenisKelamin, Alamat, NoTelp, Email, Prodi, Fakultas);
            b.setVisible(true);
            dispose(); 
        }
    }
    }//GEN-LAST:event_btnsimpanActionPerformed

