    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        if(valid.isSelected()){
            String Nama = nama.getText();
            String Nick = nick.getText();
            String ID = id.getText();
            
            String Game = game.getSelectedItem().toString();
            
            String Platform = "";
            if (pc.isSelected()){
                Platform = "PC";
            } else if(ps.isSelected()){
                Platform = "PlayStation";
            } else if(xbox.isSelected()){
                Platform = "XBOX";
            }else if(ns.isSelected()){
                Platform = "Nintendo Switch";
            }
            
            String No = no.getText();
            String Email = email.getText();
            
            hasil hs = new hasil();
            hs.info(Nama, Nick, ID, Game, Platform, No, Email); 
            hs.setVisible(true);
            dispose();
        }else {
            JOptionPane.showMessageDialog(null, "Anda harus menyetujui bahwa data sudah valid");
        }
    }//GEN-LAST:event_simpanActionPerformed

