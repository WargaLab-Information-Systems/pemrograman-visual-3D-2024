    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        String Nama = nama.getText();
        String Alamat = alamat.getText();
        String Usia = usia.getText();
        String Kwn = "";
        Boolean checklist = false;
        if(wni.isSelected()){
            Kwn = "Warga Negara Indonesia";
            
        }
        if(wna.isSelected()){
            Kwn = "Warga Negara Asing";
            checklist = true;
        }
        String Jk = (String) jk.getSelectedItem();
        String No = no.getText();
        pendakian2 ngedaki = new pendakian2();
        if(wni.isSelected()){
            if(!Nama.isEmpty() && !Alamat.isEmpty() && !Usia.isEmpty() && !No.isEmpty() && ktp.isSelected() && sks.isSelected() && sim.isSelected()){
                ngedaki.tampilkanData(Nama, Alamat, Usia, Kwn, Jk, No);
                ngedaki.setVisible(true);
                ngedaki.setChecklist(checklist);
            }
            else{
                JOptionPane.showMessageDialog(null, "Ada dokumen yang belum anda isi / belum terpenuhi", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
            }
        }
        
        if(wna.isSelected()){
            if(!Nama.isEmpty() && !Alamat.isEmpty() && !Usia.isEmpty() && !No.isEmpty() && ktp.isSelected() && sks.isSelected() && sim.isSelected() && passport.isSelected()){
                 ngedaki.tampilkanData(Nama, Alamat, Usia, Kwn, Jk, No);
                 ngedaki.setVisible(true);
                 ngedaki.setChecklist(checklist);
            }
        else{
                JOptionPane.showMessageDialog(null, "Ada dokumen yang belum anda isi", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
            }
        }
        
    }//GEN-LAST:event_simpanActionPerformed

